    private void equipActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_equipActionPerformed

        if(boxe1.getSelectedItem().equals("Journalier")){
            type.removeAllItems();
            try{
                for(int i = 0;i<= crud.affiche2(1, "NomTaf", boxe1.getSelectedItem().toString(), "IdJour", "NomJour", equip.getSelectedItem().toString()).size();i++){
                    type.addItem(crud.affiche2(1, "NomTaf", boxe1.getSelectedItem().toString(), "IdJour", "NomJour", equip.getSelectedItem().toString()).get(i).getNomJour());
                }
            }
            catch(Exception e){System.out.println(e.getMessage());}
        }
        //************
        if(boxe1.getSelectedItem().equals("Hebdomadaire")){
            type.removeAllItems();
            try{
                for(int i = 0;i<= crud.affiche2(1, "NomTaf", boxe1.getSelectedItem().toString(), "IdHebdo", "NomHebdo", equip.getSelectedItem().toString()).size();i++){
                    type.addItem(crud.affiche2(1, "NomTaf", boxe1.getSelectedItem().toString(), "IdHebdo", "NomHebdo", equip.getSelectedItem().toString()).get(i).getNomJour());
                }
            }
            catch(Exception e){System.out.println(e.getMessage());}
        }
        //************
        if(boxe1.getSelectedItem().equals("Mensuel")){
            type.removeAllItems();
            try{
                for(int i = 0;i<= crud.affiche2(1, "NomTaf", boxe1.getSelectedItem().toString().toLowerCase(), "IdMen", "NomMen", equip.getSelectedItem().toString()).size();i++){
                    type.addItem(crud.affiche2(1, "NomTaf", boxe1.getSelectedItem().toString().toLowerCase(), "IdMen", "NomMen", equip.getSelectedItem().toString()).get(i).getNomJour());
                }
            }
            catch(Exception e){System.out.println(e.getMessage());}
        }
        //************
        if(boxe1.getSelectedItem().equals("Trimestriel")||boxe1.getSelectedItem().equals("Semestriel")){
            type.removeAllItems();
            try{
                for(int i = 0;i<= crud.affiche2(1, "NomTaf", "trisem", "IdTri", "NomTri", equip.getSelectedItem().toString()).size();i++){
                    type.addItem(crud.affiche2(1, "NomTaf", "trisem", "IdTri", "NomTri", equip.getSelectedItem().toString()).get(i).getNomJour());
                }
            }
            catch(Exception e){System.out.println(e.getMessage());}
        }
    }//GEN-LAST:event_equipActionPerformed

